#define MT6366_TOP_GEN(sp)	\
{	\
	.hwirq_base = MT6366_IRQ_##sp##_BASE,	\
	.num_int_regs =	\
		((MT6366_IRQ_##sp##_BITS - 1) /	\
		MTK_PMIC_REG_WIDTH) + 1,	\
	.en_reg = MT6358_##sp##_TOP_INT_CON0,	\
	.en_reg_shift = 0x6,	\
	.sta_reg = MT6358_##sp##_TOP_INT_STATUS0,	\
	.sta_reg_shift = 0x2,	\
	.top_offset = MT6366_##sp##_TOP,	\
}

