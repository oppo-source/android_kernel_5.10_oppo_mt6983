#define MT6366_IRQ_BUCK_BASE MT6366_IRQ_VPROC11_OC
#define MT6366_IRQ_LDO_BASE MT6366_IRQ_VFE28_OC
#define MT6366_IRQ_PSC_BASE MT6366_IRQ_PWRKEY
